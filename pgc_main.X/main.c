int main(void)
{
    init_system();

    while (1)
    {
        if (false == event_queue_is_empty())
        {
            GREEN_LED_OFF;
            event_queue_run_next();
        }
        else
        {
            GREEN_LED_ON;
        }
    }

    return EXIT_SUCCESS;
}

